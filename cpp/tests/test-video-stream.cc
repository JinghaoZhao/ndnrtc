TEST(TestVideoStream, TestPublish)
{
    int w = 1280, h = 720;
    string fName = "eb_dog_"+to_string(w)+"x"+to_string(h)+"_240.yuv";
    FILE *fIn = fopen((resources_path+"/eb_samples/"+fName).c_str(), "rb");
    if (!fIn)
        FAIL() << "couldn't open input video file";

    // set up minimal NDN env
    // Set up the KeyChain.
    boost::shared_ptr<KeyChain> keyChain = boost::make_shared<KeyChain>("pib-memory:", "tpm-memory:");
    keyChain->importSafeBag(SafeBag(Name("/testname/KEY/123"),
                            Blob(DEFAULT_RSA_PRIVATE_KEY_DER, sizeof(DEFAULT_RSA_PRIVATE_KEY_DER)),
                            Blob(DEFAULT_RSA_PUBLIC_KEY_DER, sizeof(DEFAULT_RSA_PUBLIC_KEY_DER))));
    Validator validator(boost::make_shared<ValidationPolicyFromPib>(keyChain->getPib()));

    VideoStream::Settings stngs = VideoStream::defaultSettings();
    VideoStream s("/test/base/prefix", "test-stream", stngs, keyChain);

    EXPECT_EQ("test-stream", s.getStreamName());
    EXPECT_EQ("/test/base/prefix", s.getBasePrefix());
    Name streamPrefix(s.getPrefix());
    EXPECT_NO_THROW(streamPrefix[-2].toTimestamp());

    // check that packets correspond to ndnrtc v4 namespace
    int metaPacket = 0, manifestPacket = 0, dataPackets = 0, parityPackets = 0;
    int gopStart = 0, gopEnd = 0;
    int iter = 0, seq = 0, gopSeq = 0;
    vector<uint8_t> frameBuf(3*w*h/2, 0);
    while (readYUV420(fIn, w, h, frameBuf.data()))
    {
        vector<boost::shared_ptr<Data>> packets = s.processImage(ImageFormat::I420, frameBuf.data());
        EXPECT_TRUE(packets.size() > 0);

        bool hasMetaPacket = false, hasManifestPacket = false,
             hasParity = false, hasData = false;

        // check for stream meta
        bool hasStreamMeta = false;
        for (auto d : packets)
        {
            if (d->getName()[-1].toEscapedString() == "_meta")
            {
                if (d->getName()[-2].toEscapedString() == s.getStreamName())
                    hasStreamMeta = true;
                else
                {
                    // expect it's frame meta
                    EXPECT_NO_THROW(d->getName()[-2].toSequenceNumber());
                    metaPacket ++;
                    hasMetaPacket = true;
                }
            }

            if (d->getName()[-1].toEscapedString() == "_manifest")
            {
                EXPECT_NO_THROW(d->getName()[-2].toSequenceNumber());
                manifestPacket ++;
                hasManifestPacket = true;
            }

            if (d->getName()[-2].toEscapedString() == "_parity")
            {
                EXPECT_NO_THROW(d->getName()[-1].toSegment());
                parityPackets ++;
                hasParity = true;
            }

            if (d->getName()[-3].toEscapedString() == "_gop")
            {
                if (d->getName()[-1].toEscapedString() == "start")
                    gopStart ++;
                if (d->getName()[-1].toEscapedString() == "end")
                    gopEnd ++;

                gopSeq = d->getName()[-2].toSequenceNumber();
            }

            try {
                uint32_t seg = d->getName()[-1].toSegment();
                seq = d->getName()[-2].toSequenceNumber();
                hasData = true;
            }
            catch (exception&)
            {
                // keep going
            }

            // everything except payload segments must be signed
            if (!(hasData || hasParity))
                validator.validate(*d,
                    [](const Data& data){
                    },
                    [](const Data& data, const ValidationError& error){
                        FAIL() << "failed to validate data " << data.getName() << endl;
                    });
        }

        EXPECT_TRUE(hasParity);
        EXPECT_TRUE(hasData);
        EXPECT_TRUE(hasMetaPacket);
        EXPECT_TRUE(hasManifestPacket);

        if (iter == 0)
            EXPECT_TRUE(hasStreamMeta);
        else
            EXPECT_FALSE(hasStreamMeta);

        iter ++;
    }

    EXPECT_EQ(iter-1, seq);
    EXPECT_EQ(iter / stngs.codecSettings_.spec_.encoder_.gop_, gopStart);
    EXPECT_EQ(gopStart-1, gopEnd);
}

